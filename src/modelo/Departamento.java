package modelo;

public class Departamento {
	private int id_dep;
	private String nom_dep;
	
	public int getId_dep() {
		return id_dep;
	}
	public void setId_dep(int id_dep) {
		this.id_dep = id_dep;
	}
	public String getNom_dep() {
		return nom_dep;
	}
	public void setNom_dep(String nom_dep) {
		this.nom_dep = nom_dep;
	}
	
	
}
