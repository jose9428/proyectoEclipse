package modelo;

public class Denuncia {
	private int id_denuncia;
	private int id_dep;
	private String nom_dep;
	private String nom_ape;
	private String dni;
	private String correo;
	private String telefono;
	private String centro_laboral;
	private String direccion;
	private String denuncia;
	private String numero;

	public int getId_denuncia() {
		return id_denuncia;
	}
	public void setId_denuncia(int id_denuncia) {
		this.id_denuncia = id_denuncia;
	}
	public int getId_dep() {
		return id_dep;
	}
	public void setId_dep(int id_dep) {
		this.id_dep = id_dep;
	}
	public String getNom_dep() {
		return nom_dep;
	}
	public void setNom_dep(String nom_dep) {
		this.nom_dep = nom_dep;
	}
	public String getNom_ape() {
		return nom_ape;
	}
	public void setNom_ape(String nom_ape) {
		this.nom_ape = nom_ape;
	}
	public String getDni() {
		return dni;
	}
	public void setDni(String dni) {
		this.dni = dni;
	}
	public String getCorreo() {
		return correo;
	}
	public void setCorreo(String correo) {
		this.correo = correo;
	}
	public String getTelefono() {
		return telefono;
	}
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}
	public String getCentro_laboral() {
		return centro_laboral;
	}
	public void setCentro_laboral(String centro_laboral) {
		this.centro_laboral = centro_laboral;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
	public String getDenuncia() {
		return denuncia;
	}
	public void setDenuncia(String denuncia) {
		this.denuncia = denuncia;
	}
	public String getNumero() {
		return numero;
	}
	public void setNumero(String numero) {
		this.numero = numero;
	}
	
	
}
